namespace KeyEngine.Input.Pointer;

/// <summary>
/// Identifies a pointer button commonly provided by a mouse.
/// </summary>
public enum MouseButton
{
    /// <summary>
    /// The primary, usually left, button.
    /// </summary>
    Left,

    /// <summary>
    /// The secondary, usually right, button.
    /// </summary>
    Right,

    /// <summary>
    /// The middle button.
    /// </summary>
    Middle,

    /// <summary>
    /// The fourth pointer button.
    /// </summary>
    Button4,

    /// <summary>
    /// The fifth pointer button.
    /// </summary>
    Button5
}
